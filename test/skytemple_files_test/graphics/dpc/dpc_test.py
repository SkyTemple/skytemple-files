    def test_import_tile_mappings(self):
        def clone(t: TilemapEntry) -> TilemapEntry:
            return TilemapEntry.from_int(t.to_int())

        # Dungeon tilesets ALWAYS have 400 chunks.
        null_chunk = [TilemapEntry.from_int(u16(0)) for _ in range(0, 9)]
        dummy_mappings = [
            TilemapEntry(1, False, True, 0),
            TilemapEntry(2, True, False, 1),
            TilemapEntry(3, True, False, 1),
            TilemapEntry(4, True, False, 1),
            TilemapEntry(5, True, False, 1),
            TilemapEntry(6, True, False, 1),
            TilemapEntry(7, True, False, 1),
            TilemapEntry(8, True, False, 1),
            TilemapEntry(9, True, False, 1),
        ]
        dummy_mappings_corrected = [
            TilemapEntry(2, False, True, 0),
            TilemapEntry(3, True, False, 1),
            TilemapEntry(4, True, False, 1),
            TilemapEntry(5, True, False, 1),
            TilemapEntry(6, True, False, 1),
            TilemapEntry(7, True, False, 1),
            TilemapEntry(8, True, False, 1),
            TilemapEntry(9, True, False, 1),
            TilemapEntry(10, True, False, 1),
        ]

        one: DpcProtocol = self._save_and_reload_main_fixture(self.one)
        two: DpcProtocol = self._save_and_reload_main_fixture(self.one)
        three: DpcProtocol = self._save_and_reload_main_fixture(self.one)

        one.import_tile_mappings([[clone(x) for x in dummy_mappings]], True)
        two.import_tile_mappings(
            [[clone(x) for x in dummy_mappings], [clone(x) for x in dummy_mappings]],
            True,
            True,
        )
        three.import_tile_mappings([[clone(x) for x in dummy_mappings]], False, False)

        assert dummy_mappings != dummy_mappings_corrected

        def do_check(a, b, c):
            self.assertEqual(len(a.chunks), 400)
            assert_tilemap_lists_equal(dummy_mappings_corrected, a.chunks[0])
            for i in range(1, 400):
                assert_tilemap_lists_equal(null_chunk, a.chunks[i])
            self.assertEqual(len(b.chunks), 400)
            assert_tilemap_lists_equal(dummy_mappings_corrected, b.chunks[0])
            assert_tilemap_lists_equal(dummy_mappings_corrected, b.chunks[1])
            for i in range(2, 400):
                assert_tilemap_lists_equal(null_chunk, b.chunks[i])
            self.assertEqual(len(b.chunks), 400)
            assert_tilemap_lists_equal(null_chunk, c.chunks[0])
            assert_tilemap_lists_equal(dummy_mappings, c.chunks[1])
            for i in range(2, 400):
                assert_tilemap_lists_equal(null_chunk, c.chunks[i])

        do_check(one, two, three)

        saved1 = self._save_and_reload_main_fixture(one)
        saved2 = self._save_and_reload_main_fixture(two)
        saved3 = self._save_and_reload_main_fixture(three)

        do_check(saved1, saved2, saved3)

