    def test_import_tiles(self):
        dummy_tiles = [bytearray(int(DPCI_TILE_DIM * DPCI_TILE_DIM / 2))] * 5

        two = self._save_and_reload_main_fixture(self.one)
        self.one.import_tiles(list(dummy_tiles), True)
        two.import_tiles(list(dummy_tiles), False)

        self.assertEqual(dummy_tiles, self.one.tiles)
        self.assertEqual(
            [bytearray(int(DPCI_TILE_DIM * DPCI_TILE_DIM / 2))] + dummy_tiles, two.tiles
        )

        saved1 = self._save_and_reload_main_fixture(self.one)
        saved2 = self._save_and_reload_main_fixture(two)

        self.assertEqual(dummy_tiles, saved1.tiles)
        self.assertEqual(
            [bytearray(int(DPCI_TILE_DIM * DPCI_TILE_DIM / 2))] + dummy_tiles,
            saved2.tiles,
        )

    @romtest(file_names=["dungeon.bin"], path="DUNGEON/")
    def test_using_rom(self, _, file, pmd2_data):
        dbin_model = FileType.DUNGEON_BIN.deserialize(file, pmd2_data)
        for fn in dbin_model.get_files_with_ext("dpci"):
            try:
                model = self.handler.deserialize(dbin_model.get_raw(fn))
                model_reloaded = self._save_and_reload_main_fixture(model)
                self.assertEqual(model.tiles, model_reloaded.tiles)
            except Exception as e:
                raise AssertionError(f"failed processing file {fn}") from e

